namespace Assets.LogicScripts.Buildings
{
    public static class UfNameHelper
    {
        public static string Format(string name)
        {
            return name
                .Replace("A", " a")
                .Replace("B", " b")
                .Replace("C", " c")
                .Replace("D", " d")
                .Replace("E", " e")
                .Replace("F", " f")
                .Replace("G", " g")
                .Replace("H", " h")
                .Replace("I", " i")
                .Replace("J", " j")
                .Replace("K", " k")
                .Replace("L", " l")
                .Replace("M", " m")
                .Replace("N", " n")
                .Replace("O", " o")
                .Replace("P", " p")
                .Replace("Q", " q")
                .Replace("R", " r")
                .Replace("S", " s")
                .Replace("T", " t")
                .Replace("U", " U")
                .Replace("V", " v")
                .Replace("W", " w")
                .Replace("X", " x")
                .Replace("Y", " y")
                .Replace("Z", " z");
        }
    }
}